def minha_soma(num1,num2,num3): #definir a função
    return num1 + num2 + num3 #retorna a soma dos numeros

soma = minha_soma(20,30,40) #chama a função e armazena na variavel soma
print(soma)

def minha_div(n1,n2): #definir a função 
    return n1 / n2 #retorna a divisão dos numeros

div = minha_div(12,6) #chama a função e armazena na variavel div
print(div)

def minha_sub(numero1,numero2): #definir a função
    return numero1 - numero2 #retorna a subtração dos numeros

sub = minha_sub(12,3) #chama a função e armazena na variavel sub
print(sub)

def minha_mult(num1,num2): #definir a função
    return num1 * num2 #retorna a multiplicação dos numeros

mult = minha_mult(2,4) #chama a função e armazena na variavel mult
print(mult)

def minha_potence(n1,n2): #definir a funcão
    return n1 ** n2 #retorna a função de potencia

potence = minha_potence(5,5) #chama a função e armazena na variavel potence
print(potence)